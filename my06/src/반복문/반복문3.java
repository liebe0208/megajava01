package 반복문;

public class 반복문3 {

	public static void main(String[] args) {
		
		// 반복문: 초기값, 조건문, 증감식
		
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		int i = 10;
		while (i >= 1) {
			System.out.print(i + " ");
			i--;
		}

	}

}
