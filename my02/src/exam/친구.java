package exam;

public class 친구 {

	public static void main(String[] args) {
		
		//컨트롤 + 쉬프트 + f : 자동포맷팅(코딩 정리, 들여쓰기)

		int height = 180; // 키(정수)
		double weight = 60.5; // 몸무게(실수)
		char sex = '여'; // 성별
		String name = "하하하"; // 이름

		System.out.println("내 키는 " + height + "cm 입니다.");
		System.out.println("내 몸무게는 " + weight + "kg 입니다.");
		System.out.println(name + "의 성별은 " + sex + "입니다.");
		System.out.println("내 이름은 " + name + "입니다.");

	}

}
